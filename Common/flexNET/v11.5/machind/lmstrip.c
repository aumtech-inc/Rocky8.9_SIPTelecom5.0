	fprintf(stderr,
		"\t-l\tList internal and external names to be stripped\n");
	fprintf(stderr,
		"\t-e\tDon't strip external names\n");
	fprintf(stderr,
		"\t-n\tDon't strip internal and external names\n");
	fprintf(stderr,
		"\t-z\tReplaces strings with zeroes instead of random names\n");
